export function demoGenerate({ topic, mode }) {
  const clean = (topic || "").replace(/\s+/g, " ").trim();
  const title = clean.slice(0, 90) || "Изпитна тема";
  const words = [...new Set(clean.toLowerCase().replace(/[^а-яa-z0-9\s]/gi, " ").split(/\s+/).filter(w => w.length > 4))].slice(0, 10);
  const kw = words.length ? words.join(", ") : "определение, етиология, патогенеза, клиника, лечение";

  if (mode === "quiz") {
    return `ТЕСТОВИ ВЪПРОСИ: ${title}

1. Кое е най-важното при темата?
A) Да се учи без структура
B) Да се даде определение и логично обяснение
C) Да се пропусне клиниката
D) Да се говори само общо
Отговор: B

2. Коя част най-често се очаква на изпит?
A) Определение, причини, механизъм, симптоми и лечение
B) Само история
C) Само лични мнения
D) Нито едно
Отговор: A

3. Ключови понятия:
${kw}`;
  }

  if (mode === "cards") {
    return `ФЛАШКАРТИ: ${title}

Карта 1
Въпрос: Какво е основното определение?
Отговор: Дай кратко и ясно определение на темата.

Карта 2
Въпрос: Кои са причините/етиологията?
Отговор: Изброй основните фактори и ги свържи с патогенезата.

Карта 3
Въпрос: Как се проявява клинично?
Отговор: Опиши симптоми, признаци и диагностични белези.

Карта 4
Въпрос: Какво е лечението?
Отговор: Посочи принципите на поведение и терапия.

Ключови думи: ${kw}`;
  }

  if (mode === "long") {
    return `РАЗШИРЕНА ИЗПИТНА ТЕМА: ${title}

I. Определение
Темата трябва да започне с ясно определение и кратко въвеждане.

II. Етиология
Посочват се основните причини, рискови фактори и условия за развитие.

III. Патогенеза
Обяснява се механизмът на развитие стъпка по стъпка, без хаотично изброяване.

IV. Клинична картина
Описват се симптоми, обективни признаци и важни диагностични белези.

V. Диагностика
Включва анамнеза, клиничен преглед, допълнителни методи и диференциална диагноза.

VI. Лечение
Посочват се основните принципи на лечение, контрол и профилактика.

VII. Заключение
Темата е важна за правилно клинично мислене и за подреден изпитен отговор.

Ключови думи: ${kw}`;
  }

  return `КРАТКО ЗА ИЗПИТ: ${title}

1. Определение:
Дай кратко и ясно определение на темата.

2. Основни моменти:
- Етиология: кои са причините.
- Патогенеза: как се развива процесът.
- Клиника: как се проявява.
- Диагностика: как се доказва.
- Лечение: какво е поведението.

3. Ключови думи:
${kw}

4. Изпитно заключение:
Темата е важна, защото свързва теорията с практическото клинично поведение.`;
}
